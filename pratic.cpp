#include <iostream>
using namespace std; 
#include <iostream>

int main()
{
    for (int days = 12; days >= 1; days--)
    {
        switch (days)
        {
            case 12:
                std::cout << "12" << std::endl;
                break;
            case 11:
                std::cout << "11" << std::endl;
                break;
            case 10:
                std::cout << "10" << std::endl;
                break;
            case 9:
                std::cout << "9" << std::endl;
                break;
            case 8:
                std::cout << "8" << std::endl;
                break;
            case 7:
                std::cout << "7" << std::endl;
                break;
            case 6:
                std::cout << "6" << std::endl;
                break;
            case 5:
                std::cout << "5" << std::endl;
                break;
            case 4:
                std::cout << "4" << std::endl;
                break;
            case 3:
                std::cout << "3" << std::endl;
                break;
            case 2:
                std::cout << "2" << std::endl;
                break;
            case 1:
                std::cout << "1" << std::endl;
                break;
            default:
                std::cout << "Invalid value" << std::endl;
                break;
        }
    }

    return 0;
}

 
    


    
   
    





